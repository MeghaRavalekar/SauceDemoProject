//Write a Java program to Print an Array using For loop

package array_Assignments;

public class P2_PrintArray_UsingForLoop {

	public static void main(String[] args) {

		int[] a = { 14, 12, 92, 15, 11, 91 };

		for (int i = 0; i < a.length; i++) {
			System.out.print(a[i] + " ");
		}

	}
}
